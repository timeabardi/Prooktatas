Route::get('/', function () {
    return view('welcome');
});

Route::get('/dashboard', function () {
    return view('dashboard');
})->middleware(['auth', 'verified'])->name('dashboard');

Route::get('/events', [EventController::class, 'index'])->name('events.index');
Route::get('/events/create', [EventController::class, 'create'])->name('events.create');
Route::post('/events', [EventController::class, 'store'])->name('events.store');
Route::get('/events/{event}/edit', [EventController::class, 'edit'])->name('events.edit');
Route::put('/events/{event}/update', [EventController::class, 'update'])->name('events.update');
Route::delete('/events/{event}/delete', [EventController::class, 'delete'])->name('events.delete');
Route::get('/events/trash', [EventController::class, 'trash'])->name('events.trash');
Route::get('/events/{event}/restore', [EventController::class, 'restore'])->name('events.restore');
Route::delete('/events/{event}/force_delete', [EventController::class, 'force_delete'])->name('events.force_delete');

Route::middleware('auth')->group(function () {
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::patch('/profile', [ProfileController::class, 'update'])->name('profile.update');
    Route::delete('/profile', [ProfileController::class, 'destroy'])->name('profile.destroy');
});

require __DIR__.'/auth.php';

Auth::routes();

Route::get('/home', [App\Http\Controllers\HomeController::class, 'index'])->name('home');
